public class Bill {

	private String bill_id;
	private double cost;
	private String spender_id;
	private String payee_id;
	
	public Bill(){
		
	}

	public Bill(String bill_id, double cost, String spender_id, String payee_id) {
		super();
		this.bill_id = bill_id;
		this.cost = cost;
		this.spender_id = spender_id;
		this.payee_id = payee_id;
	}

	public String getBill_id() {
		return bill_id;
	}

	public void setBill_id(String bill_id) {
		this.bill_id = bill_id;
	}

	public double getCost() {
		return cost;
	}

	public void setCost(double cost) {
		this.cost = cost;
	}

	public String getSpender_id() {
		return spender_id;
	}

	public void setSpender_id(String spender_id) {
		this.spender_id = spender_id;
	}

	public String getPayee_id() {
		return payee_id;
	}

	public void setPayee_id(String payee_id) {
		this.payee_id = payee_id;
	}
	
}
