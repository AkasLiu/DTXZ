package cc.siriuscloud.dtxz.bean.vo;

public class ErrorTitleVo {
	private String submitId;
	
	private String title;						// 错题
	private String youAnswer;		// 你的错误答案
	private String corAnswer;			// 正确答案
	private String description;			// 选项？
	
	public String getSubmitId() {
		return submitId;
	}
	public void setSubmitId(String submitId) {
		this.submitId = submitId;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
	
	public String getCorAnswer() {
		return corAnswer;
	}
	public void setCorAnswer(String corAnswer) {
		this.corAnswer = corAnswer;
	}
	public void setYouAnswer(String youAnswer) {
		this.youAnswer = youAnswer;
	}
	public String getYouAnswer() {
		return youAnswer;
	}
	
	public String toString() {
		return "title: " + title + "youranswer:" + youAnswer + "coranswer2:" + corAnswer + "description" + description; 
	}
	
}
